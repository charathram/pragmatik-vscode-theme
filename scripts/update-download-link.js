#!/usr/bin/env node
"use strict";

/**
 * Rewrites the download button in docs/index.html to point at a specific
 * release's .vsix. Run by the publish workflow on each GitHub Release:
 *
 *   node scripts/update-download-link.js v0.1.0
 *
 * With no argument it restores the "latest release" default (the URL GitHub
 * always redirects to the newest release asset). The edit is confined to the
 * block between the `DL:START` / `DL:END` markers, so it is idempotent.
 */

const fs = require("fs");
const path = require("path");

const REPO = "charathram/pragmatik-vscode-theme";
const ASSET = "pragmatik.vsix";
const FILE = path.join(__dirname, "..", "docs", "index.html");

const tag = (process.argv[2] || "").trim();

const href = tag
  ? `https://github.com/${REPO}/releases/download/${tag}/${ASSET}`
  : `https://github.com/${REPO}/releases/latest/download/${ASSET}`;
const label = tag || "latest release";

const block =
  `<!-- DL:START (regenerated by scripts/update-download-link.js on each release) -->\n` +
  `      <a class="dl-btn" href="${href}">Download <span class="dl-btn-ver">${label}</span></a>\n` +
  `      <!-- DL:END -->`;

const html = fs.readFileSync(FILE, "utf8");
const re = /<!-- DL:START[\s\S]*?DL:END -->/;
if (!re.test(html)) {
  console.error("error: DL:START / DL:END markers not found in " + FILE);
  process.exit(1);
}
const next = html.replace(re, block);
fs.writeFileSync(FILE, next, "utf8");
console.log(`download link -> ${href}  (label: ${label})`);
